package io.github.aapplet.wechat.exception;

/**
 * <p>
 * `WeChatRequestException`类专门用于表示在微信相关业务操作中，发起请求时出现异常的情况，例如请求参数不正确、缺少必要参数等问题引发的异常。
 * 它继承自`WeChatException`，这使其融入到整个微信业务异常体系中，能够借助`WeChatException`已有的异常传播和处理机制，便于统一管理和处理微信业务流程中请求环节产生的各类异常，
 * 让代码在面对微信请求相关的异常时保持结构清晰、易于维护和排查问题。
 * </p>
 * <p>
 * 在实际的微信业务场景里，比如调用微信支付接口发起支付请求、向微信公众号平台发送消息请求、获取微信小程序相关数据的请求等操作过程中，
 * 当构造的请求不符合微信接口要求（像参数缺失、参数类型错误、参数值超出允许范围等情况），都可以通过抛出`WeChatRequestException`来向上层代码传达请求异常相关的信息，
 * 以便后续根据具体异常情况进行相应处理，像提示用户修正输入的参数内容、重新构造正确的请求或者记录错误日志等操作。
 * </p>
 */
public class WeChatRequestException extends WeChatException {

    /**
     * <p>
     * 这是 `WeChatRequestException` 类的基本构造方法，用于创建一个只包含异常消息的该类型异常实例。
     * 它接收一个 `message` 参数，此参数是一个字符串，用于详细描述请求异常的具体情况。
     * </p>
     * <p>
     * 当检测到微信业务请求中出现问题，并且开发人员能够明确知晓具体的异常原因，可以用一个合适的字符串来描述时，
     * 就可以使用这个构造方法。例如，若在调用微信支付接口时，缺少了必填的商户订单号参数，可构建类似“调用微信支付接口时，商户订单号参数缺失，请补充完整”这样的消息，
     * 然后将该消息作为参数传入此构造方法，创建 `WeChatRequestException` 实例并抛出，告知调用者具体的请求异常状况，方便其采取相应措施。
     * </p>
     *
     * @param message Exception message，是一个用于精确传达请求异常详细信息的字符串，
     *                它会告知调用者在发起微信相关业务请求时具体是哪方面出现了不符合预期的情况，有助于后续针对性处理。
     */
    public WeChatRequestException(String message) {
        super(message);
    }

    /**
     * <p>
     * 此构造方法用于创建一个包含详细异常消息以及导致该异常的根源异常的 `WeChatRequestException` 实例。
     * 它接收两个参数：一个是表示异常消息的字符串 `message`，另一个是表示引发该请求异常的根源异常的 `cause`（类型为 `Throwable`）。
     * </p>
     * <p>
     * 在实际的微信业务请求场景中，往往当前的请求异常是由一些底层的技术问题所引发的。例如，在构建微信支付请求参数时，
     * 若需要从配置文件中读取商户信息等相关数据，而读取配置文件过程中出现 `IOException`（文件读取异常），导致无法完整获取参数信息，进而使请求参数出现问题，
     * 此时开发人员可以根据业务层面构建相应的异常消息（比如“构建微信支付请求时，读取配置文件出错，导致参数不完整，请检查配置文件”），并将这个消息和底层的 `IOException` 异常对象
     * 通过这个构造方法传入，创建出`WeChatRequestException`实例并抛出。同样，如果是在对请求参数进行格式转换（如将用户输入的字符串类型金额转换为数字类型）时出现 `NumberFormatException`，
     * 也可以将业务层面描述该请求异常的消息以及对应的底层异常作为参数传入此构造方法来创建异常实例抛出。
     * </p>
     * <p>
     * 这样，上层代码在捕获到`WeChatRequestException`后，一方面能够通过获取异常消息了解到请求在业务层面出现的问题，
     * 另一方面还可以通过 `getCause()` 方法获取到根源异常，进而深入分析是哪个底层环节（如文件读取、数据格式转换等）出了问题，有助于更全面、准确地排查和解决请求异常情况。
     * </p>
     *
     * @param message Exception message，是从微信业务角度出发，对当前请求出现异常的情况进行描述的字符串，
     *                用于告知调用者在发起与微信服务器的交互请求过程中具体出现了何种不符合预期的情况，方便后续进行针对性处理。
     * @param cause   Throwable类型的参数，表示导致当前 `WeChatRequestException` 产生的根源异常，
     *                它承载了引发请求异常的底层技术问题相关的详细信息，例如是哪种具体的文件读取异常、数据格式转换异常等。
     */
    public WeChatRequestException(String message, Throwable cause) {
        super(message, cause);
    }

}